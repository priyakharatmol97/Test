package arrayofstring;

public class MovieGetSet 
{
	int mid,releaseYear,boxOfficeCollection;
	String movieName,producer;
	
	public void setMid(int mid)
	{
		this.mid=mid;
	}
	public int getMid()
	{
		return mid;
	}
	
	public void setReleaseYear(int releaseYear)
	{
		this.releaseYear=releaseYear;
	}
	public int getReleaseYear()
	{
		return releaseYear;
	}
	
	public void setBoxOfficeCollection(int boxOfficeCollection)
	{
		this.boxOfficeCollection=boxOfficeCollection;
	}
	public int getBoxOfficeCollection()
	{
		return boxOfficeCollection;
	}
	
	
	public void setMovieName(String movieName)
	{
		this.movieName=movieName;
	}
	public String getMovieName()
	{
		return movieName;
	}
	
	
	public void setProducer(String producer)
	{
		this.producer=producer;
	}
	public String getProducer()
	{
		return producer;
	}
	
	
	
	


}
