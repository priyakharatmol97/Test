package practiceall;

public class Date 
{
	int date,month,year;
	
	public void setdate(int date)
	{
		this.date=date;
	}
	public int getDate()
	{
		return date;
	}
	
	public void setMonth(int month)
	{
		this.month=month;
	}
	public int getMonth()
	{
		return month;
	}
	
	public void setYear(int year)
	{
		this.year=year;
	}
	public int getYear()
	{
		return year;
	}

}
