package practiceall;

public class Time extends Date
{
	int time,minute,second;
	
	public void setTime(int time)
	{
		this.time=time;
	}
	public int getTime()
	{
		return time;
	}
	
	public void setMinute(int minute)
	{
		this.minute=minute;
	}
	public int getMinute()
	{
		return minute;
	}
	
	public void setSecond(int second)
	{
		this.second=second;
	}
	public int getSecond()
	{
		return second;
	}

}
