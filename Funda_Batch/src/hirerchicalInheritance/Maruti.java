package hirerchicalInheritance;

public class Maruti 
{
	String name,color;
    int price;
    
    public void setName(String name)
    {
    	this.name=name;
    }
    public String getName()
    {
    	return name;
    }
    
    public void setColor(String color)
    {
    	this.color=color;
    }
    public String getColor()
    {
    	return color;
    }
    public void setPrice(int price)
    {
    	this.price=price;
    }
    public int getPrice()
    {
    	return price;
    }

}
